def vogal(letra):
    ehLetra = False
    if (letra == 'a' or letra == 'A'):
        ehLetra =  True
    if (letra == 'e' or letra == 'E'):
        ehLetra =  True
    if (letra == 'i' or letra == 'I'):
        ehLetra =  True
    if (letra == 'o' or letra == 'O'):
        ehLetra =  True        
    if (letra == 'u' or letra == 'U'):
        ehLetra =  True
    return ehLetra

    
